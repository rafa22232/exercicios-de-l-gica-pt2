
var turno = prompt("Digite o turno em que você estuda (M-matutino, V-Vespertino, N-Noturno):");


if (turno == "M") {
    alert("Bom Dia!");
} else if (turno == "V") {
    alert("Boa Tarde!");
} else if (turno == "N") {
    alert("Boa Noite!");
} else {
    alert("Valor Inválido!");
}
